package org.tms;

public class Calculator {

    public int sum(int num1, int num2) {
        int resultOfSum = num1 + num2;
        return resultOfSum;
    }

    public int difference(int num1, int num2) {
        int resultOfDifference = num1 - num2;
        return resultOfDifference;
    }

    public int division(int num1, int num2) {
        int resultOfDivision = num1 / num2;
        return resultOfDivision;
    }

    public int multiplication(int num1, int num2) {
        int resultOfMultiplication = num1 * num2;
        return resultOfMultiplication;
    }
}